  <div class="inner">
    <dl class="topic-guides">
      <dt>&bull; 话题标题</dt>
      <dd>请用尽可能准确的语言在标题中描述你的问题或要点。标题是必须要有的。长度最好不要超过 50 个中文字符。</dd>
      <dt>&bull; 话题内容</dt>
      <dd>在内容中对话题要点进行进一步说明，内容格式请使用 <a href="http://daringfireball.net/projects/markdown/" title="Markdown" rel="external">Markdown</a>，请勿使用任何 HTML 标签。</dd>
      <dt>&bull; 话题编辑</dt>
      <dd>在话题提交后，你可以在 15 分钟内编辑自己的话题。15 分钟之后，编辑权限将会关闭。</dd>
      <dt>&bull; 话题删除</dt>
      <dd>话题创建后，自己不能删除。只有管理员才可以删除话题。</dd>
      <dt>&bull; 不要把无聊当成有趣</dt>
      <dd>如果你抱怨生活不如意，那么你不妨回想一下之前自己无聊的时候都在干嘛。不要在所谓的 social network 和信息爆炸上浪费生命。</dd>
    </dl>
